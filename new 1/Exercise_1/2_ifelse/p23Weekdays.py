d=int(input("Enter Day of the Week  =>"))
if d==1:
    print("MONDAY")
elif d==2:
    print("TUESDAY")
elif d==3:
    print("WEDNESDAY")
elif d==4:
    print("THURSDAY")
elif d==5:
    print("FRIDAY")
elif d==6:
    print("SATURDAY")
elif d==7:
    print("SUNDAY")
else:
    print("ENTER VALID DAY")
